let currentLanguage = 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('sf-weldstar-language', lang);
    updatePageContent();
    updateLanguageSelector();
}

function updatePageContent() {
    const t = translations[currentLanguage];
    
    // æ›´æ–°é¡µé¢æ ‡é¢˜å’Œmeta
    document.title = t.pageTitle;
    document.querySelector('meta[name="description"]').content = t.metaDescription;
    document.documentElement.lang = currentLanguage;
    
    // æ›´æ–°æ‰€æœ‰å¸¦æœ‰data-translateå±æ€§çš„å…ƒç´ 
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const value = getNestedValue(t, key);
        if (value) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = value;
            } else {
                element.textContent = value;
            }
        }
    });
    
    // æ›´æ–°å­˜æ¬¾å±‚çº§è¡¨æ ¼
    updateDepositTierTable();
}

function getNestedValue(obj, path) {
    return path.split('.').reduce((current, key) => current && current[key], obj);
}

function updateDepositTierTable() {
    const t = translations[currentLanguage];
    const tableHeaders = document.querySelectorAll('.deposit-tier-table th');
    if (tableHeaders.length >= 4) {
        tableHeaders[0].textContent = t.tierTableHeaders.deposit;
        tableHeaders[1].textContent = t.tierTableHeaders.multiplier;
        tableHeaders[2].textContent = t.tierTableHeaders.credit;
        tableHeaders[3].textContent = t.tierTableHeaders.savings;
    }
}

